/**
 * Class containing constants for page and component paths used in the application.
 */
class PageConstants {
	/**
   * Base path for pages.
   * @type {string}
   * @static
   */
	static pagesPath = "";

	/**
   * Base path for components.
   * @type {string}
   * @static
   */
	static componentsPath = "";

	/**
   * Treated path for pages, dynamically configured.
   * @type {string}
   * @static
   */
	static treatedPagesPath = "";

	/**
   * Treated path for components, dynamically configured.
   * @type {string}
   * @static
   */
	static treatedComponentsPath = "";
}

export { PageConstants };
